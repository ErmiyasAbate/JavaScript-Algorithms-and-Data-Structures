/* In JavaScript, when the + operator is used with a string value,
it's called operator. */
let fullName = "Ermiyas" + " Abate";
console.log(fullName);

// OR we can use this
let ourStr = "I come first.";
ourStr += " I come second.";
console.log(ourStr);

// OR we can use this
let myName = "Ermiyas Abate";
let myStr = "My name is " + myName + " and I am very well!";
console.log(myStr);