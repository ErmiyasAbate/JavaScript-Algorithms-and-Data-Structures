/* We can easily increment or add one to a variable
with the ++ operator. 
i++; is the equivalent of i = i + 1; */
let myVar = 87;
myVar++;
console.log(myVar);
// The result will be 88


/* We can easily decrement or decrease a variable by one 
with the -- operator.
i--; is the equivalent of i = i - 1; */
let result = 54;
result--;
console.log(result);
// The result will be 53