/*
Booleans are data types may only be one of two values: true or false. 
They are basically little on-off switches, where true is on and false is off. 
These two states are mutually exclusive.

Boolean values are never written with quotes. The strings "true" and "false" are 
not Boolean and have no special meaning in JavaScript.
*/

function trueBoolean(){
    return true;
}
trueBoolean();

function falseBoolean(){
    return false;
}
falseBoolean();